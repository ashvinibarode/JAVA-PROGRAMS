public class TempConverter {
    public static void main(String[] args) {
        double c = 37;
        double f = (c * 9/5) + 32;
        double k = c + 273.15;
        System.out.println("Celsius: " + c);
        System.out.println("Fahrenheit: " + f);
        System.out.println("Kelvin: " + k);
    }
}

